# Class 1: Version Control & Git Introduction
# What is Version Control?
# Version Control is a system that helps developers track 
# and manage changes in their code over time. It allows multiple versions 
# of a project to be stored, compared, and restored whenever needed.

# Why Version Control is Important?
# Version Control is important because it helps developers manage code changes, 
# collaborate with team members, and maintain a complete history of a project.

# Problems Without Version Control
# Without Version Control, developers may lose important code changes, 
# face difficulty in collaboration, and struggle to manage multiple versions of 
# the same project.

# What is Git?
# Git is a distributed version control system that helps developers track changes 
# in their code and collaborate with others efficiently.


# What is GitHub?
# GitHub is a cloud-based platform that hosts Git repositories and allows developers to collaborate, share, and manage their code online.

