# What is Git Configuration?
# Git configuration is the process of setting up user 
# information such as name and email so that Git can identify who made changes to
# the project.

# Git Global Configuration
# Global configuration sets the default username and email for 
# all Git repositories on a system.
    
# Commands
# Set Username
# git config --global user.name "Manju"

# Set Email
# git config --global user.email "Teju@email.com"

# Verify Configuration
# git config --list

